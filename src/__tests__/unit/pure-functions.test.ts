describe("binarySearchTrigger", () => {
  it("returns the offending line when trigger is in first half", async () => {
    const lines = ["safe line 1", "BADWORD here", "safe line 3", "safe line 4"];
    const testFn = async (chunk: string) => !chunk.includes("BADWORD");
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBe("BADWORD here");
  });

  it("returns the offending line when trigger is in second half", async () => {
    const lines = [
      "safe line 1",
      "safe line 2",
      "safe line 3",
      "TRIGGER at end",
    ];
    const testFn = async (chunk: string) => !chunk.includes("TRIGGER");
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBe("TRIGGER at end");
  });

  it("returns null when entire prompt passes", async () => {
    const lines = ["safe line 1", "safe line 2", "safe line 3"];
    const testFn = async () => true;
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBeNull();
  });

  it("narrows to single line containing the trigger", async () => {
    const lines = Array.from({ length: 20 }, (_, i) =>
      i === 13 ? "line13bad" : `safe line ${i}`,
    );
    const testFn = async (chunk: string) => !chunk.includes("line13bad");
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBe("line13bad");
  });

  it("calls testFn O(log n) times, not O(n)", async () => {
    let callCount = 0;
    const lines = Array.from({ length: 100 }, (_, i) =>
      i === 50 ? "hidden-trigger" : `safe line ${i}`,
    );
    const testFn = async (chunk: string) => {
      callCount++;
      return !chunk.includes("hidden-trigger");
    };
    await binarySearchTrigger(lines, testFn);
    // log2(100) ≈ 7, plus initial full-prompt test and some overhead
    expect(callCount).toBeLessThan(20);
  });

  it("handles single-line input that triggers", async () => {
    const testFn = async (chunk: string) => !chunk.includes("X");
    const result = await binarySearchTrigger(["X"], testFn);
    expect(result).toBe("X");
  });

  it("handles empty input", async () => {
    const testFn = async () => true;
    const result = await binarySearchTrigger([], testFn);
    expect(result).toBeNull();
  });

  it("handles trigger on the first line", async () => {
    const lines = ["TRIGGER", "safe", "safe", "safe"];
    const testFn = async (chunk: string) => !chunk.includes("TRIGGER");
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBe("TRIGGER");
  });

  it("handles trigger on the last line", async () => {
    const lines = ["safe", "safe", "safe", "TRIGGER"];
    const testFn = async (chunk: string) => !chunk.includes("TRIGGER");
    const result = await binarySearchTrigger(lines, testFn);
    expect(result).toBe("TRIGGER");
  });
});

describe("resolveSessionKey", () => {
  it("prefers sessionKey over everything else", () => {
    expect(
      resolveSessionKey({
        sessionKey: "telegram:dm:+34xxx",
        sessionId: "abc-uuid",
        agentDir: "/home/x/.openclaw/agents/eva",
      }),
    ).toBe("telegram:dm:+34xxx");
  });

  it("falls back to sessionId when sessionKey is absent", () => {
    expect(
      resolveSessionKey({
        sessionId: "conv-uuid-1",
        agentDir: "/home/x/.openclaw/agents/eva",
      }),
    ).toBe("conv-uuid-1");
  });

  it("falls back to agentDir when sessionKey and sessionId are absent", () => {
    expect(
      resolveSessionKey({
        agentDir: "/home/x/.openclaw/agents/eva",
      }),
    ).toBe("/home/x/.openclaw/agents/eva");
  });

  it("falls back to 'default' when nothing is provided", () => {
    expect(resolveSessionKey({})).toBe("default");
  });

  it("yields distinct keys for two conversations of the same agent (the bug fix)", () => {
    const agentDir = "/home/x/.openclaw/agents/eva";
    const a = resolveSessionKey({
      sessionKey: "telegram:dm:userA",
      agentDir,
    });
    const b = resolveSessionKey({
      sessionKey: "telegram:dm:userB",
      agentDir,
    });
    expect(a).not.toBe(b);
  });

  it("yields the same key across turns of one conversation (resumption stability)", () => {
    const ctx = {
      sessionKey: "telegram:group:abc:user:def",
      sessionId: "rotating-uuid-on-reset",
      agentDir: "/home/x/.openclaw/agents/eva",
    };
    expect(resolveSessionKey(ctx)).toBe(resolveSessionKey(ctx));
  });

  it("treats empty string as a valid value (does not skip to fallback)", () => {
    // Empty strings are unlikely in practice but we should not silently
    // collapse them — only undefined triggers fallback.
    expect(
      resolveSessionKey({
        sessionKey: "",
        sessionId: "would-be-skipped",
      }),
    ).toBe("would-be-skipped");
  });
});

describe("deriveTurnSessionKey", () => {
  function userMsg(text: string) {
    return { role: "user" as const, content: [{ type: "text", text }] };
  }

  it("returns undefined when agentId is missing", () => {
    expect(
      deriveTurnSessionKey({
        messages: [userMsg("[agent-link · from=waka · msgId=al-1 · thread=chiki↔waka]")],
      }),
    ).toBeUndefined();
  });

  it("returns undefined when no recognised signal is present", () => {
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        systemPrompt: "Generic prompt with no agent-link / chat_id / spawn block.",
        messages: [userMsg("just a plain message body")],
      }),
    ).toBeUndefined();
  });

  it("derives an agent-link session key from the inbound header", () => {
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        messages: [
          userMsg(
            "[agent-link · from=waka · msgId=al-mozg9sv1-7420ds7p · thread=chiki↔waka]\n\n{\"tipo\":\"factura_ingesta\"}",
          ),
        ],
      }),
    ).toBe("agent:chiki:agent-link:direct:waka");
  });

  it("finds the agent-link header even when followed by a metadata user message (OpenClaw 2026.5.6+)", () => {
    // OpenClaw 2026.5.6+ splits a turn into the inbound body plus a
    // trailing `Conversation info` metadata block, both as role=user.
    // The agent-link header lives in the body (older) message; the
    // metadata block is the most recent user message. The function must
    // scan every trailing user message, not only the last one.
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        messages: [
          userMsg(
            "[agent-link · from=waka · msgId=al-mozpvwkf-96rssgx7 · thread=chiki↔waka]\n\nbody payload",
          ),
          userMsg(
            'Conversation info (untrusted metadata):\n```json\n{\n  "chat_id": "waka",\n  "message_id": "al-mozpvwkf-96rssgx7",\n  "sender_id": "waka"\n}\n```',
          ),
        ],
      }),
    ).toBe("agent:chiki:agent-link:direct:waka");
  });

  it("derives the agent-link key when the header carries in_reply_to", () => {
    // Replies from the peer keep the same `from=<peer>` token, so the
    // session key must stay stable across turns of the same conversation.
    expect(
      deriveTurnSessionKey({
        agentId: "roy",
        messages: [
          userMsg(
            "[agent-link · from=evacastro · msgId=al-mozihea8-zwmp8391 · in_reply_to=al-mozigw7y-g5zq5b2d · thread=roy↔evacastro]\n↪ Tu envío anterior (msgId al-…, hace 23s):\n> texto\n\nACK-DIAG-EVA",
          ),
        ],
      }),
    ).toBe("agent:roy:agent-link:direct:evacastro");
  });

  it("does not falsely match a Telegram payload that mentions agent-link in body", () => {
    // The agent-link branch is anchored to start-of-text; a Telegram
    // message that quotes "agent-link" mid-body must not divert to the
    // wrong key. The chat_id branch should still pick this up.
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        messages: [
          userMsg(
            'Hi, I read about [agent-link · from=...] in the docs.\n```json\n{"chat_id": "telegram:540382330"}\n```',
          ),
        ],
      }),
    ).toBe("agent:chiki:telegram:direct:540382330");
  });

  it("derives a Telegram direct chat key from chat_id JSON (regression)", () => {
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        messages: [
          userMsg('```json\n{"chat_id": "telegram:8426343632"}\n```'),
        ],
      }),
    ).toBe("agent:chiki:telegram:direct:8426343632");
  });

  it("derives a Telegram supergroup key from a -100… chat_id (regression)", () => {
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        messages: [
          userMsg('```json\n{"chat_id": "telegram:-1005229099225"}\n```'),
        ],
      }),
    ).toBe("agent:chiki:telegram:supergroup:5229099225");
  });

  it("derives the legacy spawn key from `Agent 2 (target) session:` (regression)", () => {
    expect(
      deriveTurnSessionKey({
        agentId: "evacastro",
        systemPrompt:
          "Agent-to-agent message context\nAgent 1 (requester) session: agent:roy:main\nAgent 2 (target) session: agent:evacastro:main",
      }),
    ).toBe("agent:evacastro:main");
  });

  it("prefers the legacy spawn key over the agent-link header when both are present", () => {
    // Defence in depth: if a future pipeline somehow surfaces both
    // signals at once, the explicit `Agent 2 (target) session:` line is
    // authoritative because it is constructed by the gateway, not by a
    // plugin's user-facing prompt.
    expect(
      deriveTurnSessionKey({
        agentId: "chiki",
        systemPrompt:
          "Agent 2 (target) session: agent:chiki:custom:scope",
        messages: [userMsg("[agent-link · from=waka · msgId=al-1 · thread=chiki↔waka]")],
      }),
    ).toBe("agent:chiki:custom:scope");
  });
});
